// creando un condicional if
const puntaje = 1000;

if (puntaje == 1000) { // condicional if que revisa que el puntaje sea el mismo al que se revisa. Si es igual se ejecuta la siguiente linea
    console.log('Si es igual'); // se ejecutara este clg en caso de ser verdadera la condicion
}else{
    // en caso de que la condicion regrese false, se ejecutara esta siguiente linea de codigo
    console.log('No, no es igual');
}