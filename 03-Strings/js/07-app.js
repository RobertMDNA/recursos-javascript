// String Methods - toLowerCase & toUpperCase
const producto = 'Monitor 20 Pulgadas';

// metodo toUpperCase - permite convertir todos los caracteres de una cadena de texto en mayusculas
console.log(producto.toUpperCase()); // esto mostrara "MONITOR 20 PULGADAS"

// metodo toLowerCase - permite convertir todos los caracteres de una cadena de texto en minusculas
console.log(producto.toLowerCase()); // esto mostrara "monitor 20 pulgadas"

// convertir un numero a string
const precio = 300;
console.log(precio.toString()); // esto convertira un numero a un string