// crear arrays
// un arreglo almacena informacion de la cual agrupa varios elementos
// esta es la primer forma y mas conocida de crear arreglos
const numeros = [10, 20, 30]; // en javascript siempre que se vea esta sintaxis de corchete significa que es un array

// segunda forma de crear arreglos menos utilizada
const meses = new Array('Enero', 'Febrero', 'Marzo');

// mostrando ambos arreglos por pantalla
console.log(numeros);
console.log(meses);

// creando arreglo con diferentes tipos de elementos y mostrando por pantalla
const deTodo = ['Hola', 10, true, 'si', null, undefined, {nombre: 'Roberto', trabajo: 'Desarrollador FrontEnd'}];
console.log(deTodo);