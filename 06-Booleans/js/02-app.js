// comparando valores booleanos
const boolean1 = true;
const boolean2 = false;

// comparando ambos valores
console.log(boolean1 == boolean2);
console.log(boolean1 === boolean2); // para ambos casos sera falso

// comparando si un valor es igual a otro valor
console.log(boolean1 === true); // deberia de ser true, ya que si son iguales