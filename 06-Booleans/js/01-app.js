// creando y comparando booleans
const boolean1 = true;
const boolean2 = false;

console.log(boolean1);
console.log(boolean2);

// mostrando su tipo de dato
console.log(typeof boolean1);
console.log(typeof boolean2);

// otra forma de crear un boolean
const boolean3 = new Boolean(true); // esta variable tendra el valor boolean de true
// sin embargo, sera un objeto
console.log(typeof boolean3);