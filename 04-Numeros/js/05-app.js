// incremento y decremento en javascript
let puntaje = 5;

puntaje++; // se manda a llamar puntaje y despues se le suma uno, en este no se mostrara directo

++puntaje; // se suma uno y despues se manda a llamar, este el resultado se mostrara directo

// esto tambien aplica para los decrementos
puntaje--;

--puntaje;

// tambien podemos incrementar o decrementar en una cierta cantidad que queramos
puntaje+=5; // a puntaje le sumara 5

puntaje-=2; // a puntaje le restara 2