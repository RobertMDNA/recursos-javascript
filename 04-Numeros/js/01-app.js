// crear numeros en javascript
const numero1 = 20; // este es un dato numerico
const numero2 = '20'; // este es un dato numerico siendo un string

// para javascript todos numero entero negativo, positivo o con decimales es tratado como numero. No existe float ni double
const numero3 = -20; // este es un dato numerico
const numero4 = 20.20; // este tambien es un dato de tipo numerico

// otra sintaxis no muy comun para crear numeros es la siguiente
const numero5 = new Number(80);

console.log(numero1);
console.log(numero2);
console.log(numero3);
console.log(numero4);
console.log(numero5);